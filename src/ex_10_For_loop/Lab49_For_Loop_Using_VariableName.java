package ex_10_For_loop;

public class Lab49_For_Loop_Using_VariableName {
    public static void main(String[] args) {

        //We can use variable names as well instead of characters.
        for (int siri = 0; siri < 10; siri++) {
            System.out.println(siri);


         //We can use numbers(positive and negative) as well
            for (int _1 = 0; _1 <= 10; _1++) { // 11 times
                System.out.println(_1); // 0 to 10
            }
        }
    }
}
