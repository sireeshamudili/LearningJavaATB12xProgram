package ex_10_For_loop;

public class Lab51_For_If_Loop {
    public static void main(String[] args) {

        for(int siri=0; siri < 18; siri++){ // 0 to 17, 18 times
            if (siri > 15){
                System.out.println("Gift from Dad, IPhone");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
