package ex_16_Arrays;

public class Lab86_Arrays_Strings {
    public static void main(String[] args) {

        String[] names =  {"siri","nithya","rakhi"};
        System.out.println(names.length);
        System.out.println(names[2]);

        String[] name_list = new String[3];
        name_list[0] = "pavan";
        name_list[1] = "prabha";
        name_list[2] = "Ram";

        System.out.println(name_list.length);
        System.out.println(name_list[1]);
    }
}
