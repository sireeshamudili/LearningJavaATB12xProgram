package ex_01_JavaBasics_Part1;

public class Lab10_PrintlnVsPrint {
    public static void main(String[] args) {
        // println adding new line automatically
        System.out.println("Hi");
        System.out.println("Hello");

        // print does not add new line in this case output attach in one line
        System.out.print("Hi");
        System.out.print("Hello"); //Gives in the same line
    }
}
