package ex_07_Incr_Decr_Operator;

public class Lab37_Pre_Increment_Operator {
    public static void main(String[] args) {

        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // ++a ->  a = a+1 , increment by 1

        //  Exp and Result Table(ERT)
        // Line No | a | Result b
        // 8   |  10 |  NA
        // 9    | 11  |  11
        // 10   | 11 - print | 11
        // 11   | 11 | 11 - pint


    }
}
