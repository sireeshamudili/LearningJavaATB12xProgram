package ex_04_Operators;

public class Lab26_BODMAS_Rule {
    public static void main(String[] args) {

        // BODMAS - Bracket of Div, mul, add, sub

        System.out.println((9 * 3 / 9 + 1) * 3);

        // 9 * 3 -> 27
        // 27/9 -> 3
        // 3+1 -> 4
        // 4 * 3 -> 12

    }
}
