package interview_QnA;

public class Lab04_Char_Int {
    public static void main(String[] args) {

        //Char always Indicates Integral values
        char a1 = 'A'; // ASCII -> 65
        char b1 = 'B'; // 66

        System.out.println(a1 + b1); // a1+b1 - 65+66 -> 131
        System.out.println('A' == 65);
    }
}
